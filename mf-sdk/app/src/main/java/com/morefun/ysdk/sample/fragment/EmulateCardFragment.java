    private void open() {
        try {
            int ret = DeviceHelper.getEmulateCardHandler().open();

            if (ret == 0) {
                bStop = false;
                DialogUtils.showAlertDialog(getActivity(), "Open Success");
            } else {
                DialogUtils.showAlertDialog(getActivity(), "Open Fail");
            }
        } catch (RemoteException e) {
            e.printStackTrace();
        }
    }

    private void close() {
        try {
            bStop = true;
            int ret = DeviceHelper.getEmulateCardHandler().close();

            if (ret == 0) {
                DialogUtils.showAlertDialog(getActivity(), "Close Success");
            } else {
                DialogUtils.showAlertDialog(getActivity(), "Close Fail");
            }
        } catch (RemoteException e) {
            e.printStackTrace();
        }
    }

